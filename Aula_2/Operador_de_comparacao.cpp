#include <stdio.h>

int main() {
    int a = 5, b = 10;

    printf("a == b: %d\n", a == b);  // Verifica se a é igual a b
    printf("a != b: %d\n", a != b);  // Verifica se a é diferente de b
    printf("a > b: %d\n", a > b);    // Verifica se a é maior que b
    printf("a < b: %d\n", a < b);    // Verifica se a é menor que b
    printf("a >= b: %d\n", a >= b);  // Verifica se a é maior ou igual a b
    printf("a <= b: %d\n", a <= b);  // Verifica se a é menor ou igual a b

    return 0;
}
