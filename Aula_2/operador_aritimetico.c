#include <stdio.h>

int main() {
    int x = 15, y = 4;

    printf("x + y = %d\n", x + y);  // Soma
    printf("x - y = %d\n", x - y);  // Subtração
    printf("x * y = %d\n", x * y);  // Multiplicação
    printf("x / y = %d\n", x / y);  // Divisão (inteira)
    printf("x %% y = %d\n", x % y); // Módulo (resto da divisão)

    return 0;
}
